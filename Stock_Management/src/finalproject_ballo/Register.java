    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String firstname = jTextField1.getText().trim();
        String lastname = jTextField2.getText().trim();
        String phonenumber = jTextField3.getText().trim();
        String username = jTextField4.getText().trim();
        String password = new String(jPasswordField1.getPassword());

        if (firstname.isEmpty() || lastname.isEmpty() || phonenumber.isEmpty() || username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Hash the password
        String hashedPassword = hashPassword(password);
        if (hashedPassword == null) return;

        // MySQL DB Config
        String url = "jdbc:mysql://localhost:3306/stockmanagement";
        String dbUser = "root";
        String dbPass = "root";

        String sql = "INSERT INTO users (Firstname, Lastname, Phonenumber, Username, Password) VALUES (?, ?, ?, ?, ?)";

        try (Connection conn = DriverManager.getConnection(url, dbUser, dbPass);
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, firstname);
            stmt.setString(2, lastname);
            stmt.setString(3, phonenumber);
            stmt.setString(4, username);
            stmt.setString(5, hashedPassword);

            int rows = stmt.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Registration Successful!");
                new Login().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Registration Failed", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

