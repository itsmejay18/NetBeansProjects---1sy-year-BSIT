    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Get the entered data
    String firstname = jTextField1.getText();
    String lastname = jTextField2.getText();
    String phonenumber = jTextField3.getText();
    String username = jTextField4.getText();
    String password = new String(jPasswordField1.getPassword());
    
    // Check if the fields are not empty
    if (!firstname.isEmpty() && !lastname.isEmpty() && !phonenumber.isEmpty() && !username.isEmpty() && !password.isEmpty()) {
        
        // Database connection parameters for "Local instance MySQL80"
        String url = "jdbc:mysql://localhost:3306/java";  // Adjust the database URL
        String dbUsername = "root";  // Your MySQL username
        String dbPassword = "root";  // Your MySQL password
        
        // SQL query to insert data into the 'idjava_users' table
        String sql = "INSERT INTO java_users (Firstname, Lastname, Phonenumber, Username, Password) VALUES (?, ?, ?, ?, ?)";
        
        try {
            // Establish the connection to the database
            Connection conn = DriverManager.getConnection(url, dbUsername, dbPassword);

            // Create a PreparedStatement to insert the data
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, firstname);
            stmt.setString(2, lastname);
            stmt.setString(3, phonenumber);
            stmt.setString(4, username);
            stmt.setString(5, password);  // Consider hashing the password in a real application

            // Execute the insert statement
            int rowsAffected = stmt.executeUpdate();

            // Check if the registration was successful
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Registration Complete");
            } else {
                JOptionPane.showMessageDialog(this, "Registration Failed", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Close the statement and connection
            stmt.close();
            conn.close();
            
        } catch (SQLException e) {
            // Handle any database connection or query errors
            JOptionPane.showMessageDialog(this, "Database Connection Failed: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        // Close the registration form and show the display window
        
        
    } else {
        // Show error message if any field is empty
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

